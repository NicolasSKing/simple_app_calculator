class SimpleAppCalculator:

    def addition(self, first_number, second_number):
        answer = first_number + second_number
        print(answer)

    def subtract(self, first_number, second_number):
        answer = first_number - second_number
        print(answer)

    def multiplication(self, first_number, second_number):
        answer = first_number * second_number
        print(answer)

